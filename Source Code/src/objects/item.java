package objects;

public class item {
    public String name;
    public String description;
    public int xp;
    public String field;
    public int buff;

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public int getXp() {
        return xp;
    }

    public String getField() {
        return field;
    }

    public int getBuff() {
        return buff;
    }
}
