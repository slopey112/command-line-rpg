package objects;

public class item {
    protected String name;
    protected String description;
    protected int xp;
    protected String field;
    protected int buff;

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public int getXp() {
        return xp;
    }

    public String getField() {
        return field;
    }

    public int getBuff() {
        return buff;
    }
}
